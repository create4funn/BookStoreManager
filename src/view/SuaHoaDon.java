    public ChiTietHoaDon findCTHD(int maHD) {
        for (ChiTietHoaDon i : cthd) {
            if (maHD == i.getMaSach()) {
                return i;
            }

        }
        return null;
    }

    public Sach findSach(int maS) {
        for (Sach i : sachList) {
            if (maS == i.getMaSach()) {
                return i;
            }
        }
        return null;
    }

