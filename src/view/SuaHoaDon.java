    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        int i_row = tblSachMua.getSelectedRow();
        if (i_row == -1) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn sản phẩm để xoá sửa số lượng !");
        } else {
            String newSL = JOptionPane.showInputDialog(this, "Nhập số lượng cần thay đổi", "Thay đổi số lượng", QUESTION_MESSAGE);
            if (newSL != null) {
                int soLuong;
                try {
                    soLuong = Integer.parseInt(newSL);
                    if (soLuong > 0) {
                        if (soLuong > findSach(cthd.get(i_row).getMaSach()).getSoLuong()) {
                            JOptionPane.showMessageDialog(this, "Số lượng sách không đủ !");
                        } else {
                            cthd.get(i_row).setSoLuong(soLuong);
                            cthd.get(i_row).setThanhTien(
                                    cthd.get(i_row).getSoLuong()* findSach(cthd.get(i_row).getMaSach()).getGiaBan()
                            );
                            loadDataToTableSachMua();
                            
                        }
                    } else {
                        JOptionPane.showMessageDialog(this, "Vui lòng nhập số lượng lớn hơn 0");

                    }
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(this, "Vui lòng nhập số lượng ở dạng số nguyên!");
                }
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

